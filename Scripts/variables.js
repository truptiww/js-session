// dynamically typed language
var num = 67; //number
document.write(num);
console.log(num);
console.log(typeof num);
document.write('<br>');
var num = 43.5; //number
document.write(num);
console.log(num);
console.log(typeof num);
document.write('<br>');
var num = 'Hello';
document.write(num);
console.log(num);
console.log(typeof num);
document.write('<br>');
var num = true;
document.write(num);
console.log(num);
console.log(typeof num);

var college = 'Sk College';
var college = 'DK College';
console.log(college);
// var type variables can be redeclared

let company = 'Neo';
company = 'SOFT';
console.log(company);

// let type variables can not be redeclared, you can reassign the value

const pie = '3.14';
console.log(pie); //can not redeclared and can not reassigned
